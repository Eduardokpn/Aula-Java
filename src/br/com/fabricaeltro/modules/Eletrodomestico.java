package br.com.fabricaeltro.modules;

public class Eletrodomestico {
	
	private String marcaNovais;
	private String modeloNovais;
	private double pesoNovais;
	private int quantidadePecasNovais;
	public String getMarcaNovais() {
		return marcaNovais;
	}
	public void setMarcaNovais(String marcaNovais) {
		this.marcaNovais = marcaNovais;
	}
	public String getModeloNovais() {
		return modeloNovais;
	}
	public void setModeloNovais(String modeloNovais) {
		this.modeloNovais = modeloNovais;
	}

	public double getPesoNovais() {
		return pesoNovais;
	}
	public void setPesoNovais(double pesoNovais) {
		this.pesoNovais = pesoNovais;
	}
	public int getQuantidadePecasNovais() {
		return quantidadePecasNovais;
	}
	public void setQuantidadePecasNovais(int quantidadePecasNovais) {
		this.quantidadePecasNovais = quantidadePecasNovais;
	}
	

}
